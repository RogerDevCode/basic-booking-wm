// SEMANTIC SAMPLING
// ============================================================================

const DEFAULT_K = 3;

export function selectFewShotExamples(
  userMessage: string,
  k: number = DEFAULT_K,
): readonly ScoredExample[] {
  const data = loadEmbeddings();
  const firstEx = data.examples[0];
  const hasRealEmbeddings = data.examples.length > 0 && firstEx != null && firstEx.embedding.length > 0;

  let queryEmbedding: number[];

  if (hasRealEmbeddings) {
    // Pre-computed embeddings available — we need to embed the user message
    // Since we can't run the model at runtime, use keyword embedding as approximation
    // This is a known limitation — the Python script should be run to generate real embeddings
    queryEmbedding = keywordEmbedding(userMessage);
  } else {
    // Fallback: keyword-based embedding
    queryEmbedding = keywordEmbedding(userMessage);
  }

  // Score all examples
  const scored: ScoredExample[] = data.examples.map((ex) => ({
    text: ex.text,
    intent: ex.intent,
    similarity: cosineSimilarity(queryEmbedding, ex.embedding),
  }));

  // Sort by similarity (descending) and take top-k
  scored.sort((a, b) => b.similarity - a.similarity);
  return scored.slice(0, Math.min(k, scored.length));
}

// ============================================================================
// FORMAT FEW-SHOT EXAMPLES FOR PROMPT
// ============================================================================

export function formatFewShotExamples(
  examples: readonly ScoredExample[],
): string {
  if (examples.length === 0) return '';

  const lines = examples.map((ex) => {
    const intent = ex.intent as typeof INTENT[keyof typeof INTENT];
    return `User: "${ex.text}"\n→ {"intent":"${intent}","confidence":0.90,"entities":{},"needs_more":false,"follow_up":null}`;
  });

  return lines.join('\n\n');
}

