let cachedEmbeddings: FewShotEmbeddingData | null = null;

function loadEmbeddings(): FewShotEmbeddingData {
  if (cachedEmbeddings != null) return cachedEmbeddings;

  // Try to load from JSON file (generated by Python script)
  // In Windmill, this file would be bundled with the script
  try {
    // eslint-disable-next-line @typescript-eslint/no-require-imports, @typescript-eslint/no-var-requires
    const data = require('./fewshot-embeddings.json') as FewShotEmbeddingData;
    const firstEx = data.examples[0];
    if (data.examples.length > 0 && firstEx != null && firstEx.embedding.length > 0) {
      cachedEmbeddings = data;
      return data;
    }
  } catch {
    // JSON file not available — use fallback (keyword-based)
  }

  cachedEmbeddings = FALLBACK_EMBEDDINGS;
  return FALLBACK_EMBEDDINGS;
}

